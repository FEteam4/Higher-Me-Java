package org.example.coding.domain;

public class Blank {
    private int x;
    private int y;
    private String answer;

    public Blank(int x, int y, String answer) {
        this.x = x;
        this.y = y;
        this.answer = answer;
    }

    public int getX() {
        return x;
    }


    public int getY() {
        return y;
    }

    public String getAnswer() {
        return answer;
    }
}